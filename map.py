class Map:
    def __init__(self, game, level=1):
        self.game = game
        self.level = level
        self.mini_map = self.load_level_map(level)
        self.world_map = {}
        self.rows = len(self.mini_map)
        self.cols = len(self.mini_map[0])
        self.get_map()

    def load_level_map(self, level):
        if level == 1:
            return mini_map_level1
        elif level == 2:
            return mini_map_level2
        elif level == 3:
            return mini_map_level3
        return mini_map_level1

    def get_map(self):
        for j, row in enumerate(self.mini_map):
            for i, value in enumerate(row):
                if value:
                    self.world_map[(i, j)] = value
